package com.compete.vo;

public class Keyword {

	private String total_time_index;
	private String keyword;
	private String natural_share;
	private String paid_share;
	private String average_time_index;
	private String search_referral_share;

	public String getTotal_time_index() {
		return total_time_index;
	}

	public void setTotal_time_index(String total_time_index) {
		this.total_time_index = total_time_index;
	}

	public String getKeyword() {
		return keyword;
	}

	public void setKeyword(String keyword) {
		this.keyword = keyword;
	}

	public String getNatural_share() {
		return natural_share;
	}

	public void setNatural_share(String natural_share) {
		this.natural_share = natural_share;
	}

	public String getPaid_share() {
		return paid_share;
	}

	public void setPaid_share(String paid_share) {
		this.paid_share = paid_share;
	}

	public String getAverage_time_index() {
		return average_time_index;
	}

	public void setAverage_time_index(String average_time_index) {
		this.average_time_index = average_time_index;
	}

	public String getSearch_referral_share() {
		return search_referral_share;
	}

	public void setSearch_referral_share(String search_referral_share) {
		this.search_referral_share = search_referral_share;
	}

}
