package com.compete.vo;

public class IncomingTraffic {

	private String incoming_referral;
	private String visits;
	private String delta_percentage;
	private String delta;
	private String delta_share;
	private String share;

	public String getIncoming_referral() {
		return incoming_referral;
	}

	public void setIncoming_referral(String incoming_referral) {
		this.incoming_referral = incoming_referral;
	}

	public String getVisits() {
		return visits;
	}

	public void setVisits(String visits) {
		this.visits = visits;
	}

	public String getDelta_percentage() {
		return delta_percentage;
	}

	public void setDelta_percentage(String delta_percentage) {
		this.delta_percentage = delta_percentage;
	}

	public String getDelta() {
		return delta;
	}

	public void setDelta(String delta) {
		this.delta = delta;
	}

	public String getDelta_share() {
		return delta_share;
	}

	public void setDelta_share(String delta_share) {
		this.delta_share = delta_share;
	}

	public String getShare() {
		return share;
	}

	public void setShare(String share) {
		this.share = share;
	}

}
